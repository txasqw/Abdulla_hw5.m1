public class Boss {
    private int Helf;
    private int uron;
    private String defense;

    public int getHelf() {
        return Helf;
    }

    public void setHelf(int helf) {
        Helf = helf;
    }

    public int getUron() {
        return uron;
    }

    public void setUron(int uron) {
        this.uron = uron;
    }

    public String getDefense() {
        return defense;
    }

    public void setDefense(String defense) {
        this.defense = defense;
    }

}
